package model;

public class Esc_Bol_Set {
	
	private int codigoBolsista;
	private int codigoSetor;
	private int codEscala;
	
	
	public int getCodigoBolsista() {
		return codigoBolsista;
	}
	public void setCodigoBolsista(int codigoBolsista) {
		this.codigoBolsista = codigoBolsista;
	}
	public int getCodigoSetor() {
		return codigoSetor;
	}
	public void setCodigoSetor(int codigoSetor) {
		this.codigoSetor = codigoSetor;
	}
	public int getCodEscala() {
		return codEscala;
	}
	public void setCodEscala(int codEscala) {
		this.codEscala = codEscala;
	}

}
